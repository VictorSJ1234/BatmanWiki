const villain1 = document.querySelector('#villain1_modal');
const villain1Photo = document.querySelector('#villain1');
const villain1Button = document.querySelector('#villain1_button');
const closeBtn = document.querySelector('#close_villain1');

villain1Button.addEventListener('click', villain1Open);
function villain1Open(){
  villain1.style.display = 'block';
}
villain1Photo.addEventListener('click', villain1Open);
function villain1Open(){
  villain1.style.display = 'block';
}
closeBtn.addEventListener('click', closeVillain1);
function closeVillain1(){
  villain1.style.display = 'none';
}

//hover to change the border of a picture
villain1Button.addEventListener('mouseover', ()=>{
  villain1Photo.style.borderColor = 'cyan';
});
villain1Button.addEventListener('mouseout', ()=>{
  villain1Photo.style.borderColor = 'black';
});
villain1Photo.addEventListener('mouseover', ()=>{
  villain1Photo.style.borderColor = 'cyan';
});
villain1Photo.addEventListener('mouseout', ()=>{
  villain1Photo.style.borderColor = 'black';
});

window.addEventListener('click', outsideClick);
function outsideClick(e){
  if (e.target == villain1){
    villain1.style.display = 'none';
  }
}

